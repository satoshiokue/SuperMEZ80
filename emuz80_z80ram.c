//Address Bus
union {
	unsigned int w;			//16 bits Address
	struct {
		unsigned char l;	//Address low
		unsigned char h;	//Address high
	};
} ab;


// UART3 Transmit
void putch(char c) {
	while(!U3TXIF); // Wait or Tx interrupt flag set
	U3TXB = c; // Write data
}
